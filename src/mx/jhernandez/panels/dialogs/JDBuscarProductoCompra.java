    private void jtProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtProductoMouseClicked
        try {
            Producto pro = BDProducto.buscarProducto(Integer.parseInt(String.valueOf(jtProducto.getModel().getValueAt(jtProducto.getSelectedRow(), 0))));
            FrmV.setCodigopro(pro.getnProCodigo());
            FrmV.setNombre(pro.getcProNombre());
            FrmV.setDescripcionpro(pro.getcProDescripcion());
            FrmV.setPreciounitprov(pro.getnProPRecioVenta());
            FrmV.setStock(pro.getnProCantidad());
            FrmV.txtCodigoPro.setText(String.valueOf(FrmV.getCodigopro()));
            FrmV.txtStockPro.setText(String.valueOf(FrmV.getStock()));
            FrmV.txtNombrePro.setText(String.valueOf(FrmV.getNombre()));
        } catch (SQLException ex) {
            System.out.println();
        }
    }//GEN-LAST:event_jtProductoMouseClicked

