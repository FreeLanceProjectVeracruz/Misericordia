    private void actualizarBusqueda() throws SQLException {
        ArrayList<Producto> result;
        if (String.valueOf(cboParametroPro.getSelectedItem()).equalsIgnoreCase("Codigo")) {
            result = BDProducto.listarProductoPorCodigoEstado(txtBuscarPro.getText());
        } else if (String.valueOf(cboParametroPro.getSelectedItem()).equalsIgnoreCase("Nombre")) {
            result = BDProducto.listarProductoPorNombreEstado(txtBuscarPro.getText());
        } else {
            result = BDProducto.listarProductoPorCodProveedorEstado(txtBuscarPro.getText());
        }
        recargarTable(result);
    }

    private void recargarTable(ArrayList<Producto> list) {
        Object[][] datos = new Object[list.size()][6];
        int i = 0;

        for (Producto p : list) {
            datos[i][0] = p.getnProCodigo();
            datos[i][1] = p.getcCodProProv();
            datos[i][2] = p.getcProNombre();
            datos[i][3] = p.getcProDescripcion();
            datos[i][4] = p.getnProPRecioVenta();
            datos[i][5] = p.getnProCantidad();
            i++;
        }

        jtProducto.setModel(new javax.swing.table.DefaultTableModel(datos,
                new String[]{
                    "CODIGO", "CODPROD-PROVEEDOR", "NOMBRE", "DESCRIPCION", "PRECIO VENTA", "STOCK"
                }) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    }
                });
    }

